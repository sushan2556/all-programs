#Program to check if the names are present in the list or not
names = ["Sushant" , "Shweta", "Mangesh", ] #list of names created in list
name = input("Enter the name that you need to check ")
if (name in names): # in function used 
    print("Your name is present in the list")
else:
    print("Your name is not present in the list")

